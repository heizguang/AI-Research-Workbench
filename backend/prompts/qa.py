"""
问答智能体提示词
QA 流程使用的所有提示词模板
"""


# ── Step 1：分类（判断报告是否足以回答问题）──────────────────

def classify(report: str, question: str) -> str:
    """判断报告内容是否足以回答用户问题，返回'能'或'不能'"""
    return f"""你是智能问答助手。请判断以下报告内容是否足以完整回答用户的问题。

判断规则：
1. 如果报告内容包含了回答问题所需的所有信息 → 回答"能"
2. 如果问题涉及报告中没有提到的实体、话题或领域 → 回答"不能"
3. 如果问题部分与报告相关，但需要补充报告之外的信息才能完整回答 → 回答"不能"

报告内容：
{report}

用户问题：{question}

请只回答"能"或"不能"，不要解释。"""


# ── Step 2a：基于报告回答（不需要搜索）──────────────────────

def answer_from_report(report: str, question: str, history_text: str) -> str:
    """仅基于报告内容回答"""
    return f"""你是智能问答助手。请基于以下报告内容回答用户的问题。

重要规则：
- 如果报告内容足以回答问题，直接回答即可，不需要标注任何来源
- 如果报告内容不足以完整回答问题，请在回答中明确说明"报告中未涉及以下内容"，然后基于你的知识补充回答
{history_text}
报告内容：
{report}

用户问题：{question}

请回答："""


# ── Step 2b：综合回答（报告 + 搜索结果）─────────────────────

def answer_from_combined(report: str, search_context: str, question: str) -> str:
    """综合报告和搜索结果回答，末尾带参考链接"""
    return f"""你是智能问答助手。请综合以下信息回答用户的问题。

重要规则：
- 正文中不要标注 [来源：xxx]，直接给出回答内容即可
- 在回答末尾添加"## 参考来源"章节，列出你参考过的搜索结果链接，格式为：
  1. **标题** — [查看原文](链接地址)
  2. **标题** — [查看原文](链接地址)
  注意：只使用搜索结果中提供的原始链接，严禁编造任何 URL。不要重复相同的链接，每个链接只出现一次
- 如果报告和搜索结果有冲突，以搜索结果为准

报告内容：
{report if report else '无'}

网络搜索结果：
{search_context}

用户问题：{question}

请综合以上信息，给出完整、准确的回答："""
